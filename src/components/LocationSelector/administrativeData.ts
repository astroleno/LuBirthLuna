// 中国省市区行政层级数据结构

export interface Province {
  code: string;
  name: string;
  shortName: string;
  type: 'province' | 'municipality' | 'autonomous_region' | 'special_administrative_region';
}

export interface City {
  code: string;
  name: string;
  provinceCode: string;
  type: 'city' | 'prefecture' | 'league' | 'district';
}

export interface District {
  name: string;
  cityName: string;
  provinceName: string;
  lon: number;
  lat: number;
}

export interface AdministrativeData {
  provinces: Province[];
  cities: City[];
  districts: District[];
}

